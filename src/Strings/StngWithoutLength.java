package Strings;

public class StngWithoutLength {
	
	public static void main(String[] args) {
		String s ="HelloJava";
		char [] ch = s.toCharArray();
		int count=0;
		for(int i=0; i< ch.length; i++)
		{
			count++;
		}
		
		System.out.println("Length of the string is: "+ count);
	}
	
	
	
	
	
	
	
	
	

}
